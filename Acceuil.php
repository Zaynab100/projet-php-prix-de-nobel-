<h3>Syntaxe de manipulation du site Web :</h3>
Comme il est clairement représenté, On est sur un site web qui comporte quatre rubriques différentes, chacune ayant une fonction spécifique.

<br><br>



    La première rubrique est celle sur laquelle on est, c'est bien la page d'accueil qui contient de diverses informations sur le site web réalisé et sert de point de départ pour sa navigation.

<br><br>



    Les 3 autres rubriques sont liées à la gestion des informations. Voici ci-dessous la syntaxe de leur manipulation bien détaillée.

<br><br>



    La deuxième rubrique est la rubrique "Lauréat", qui comprend trois pages différentes "afficher", "ajouter" et "supprimer" auxquels on a un accès direct à partir du haut de la page. Comme on a un bouton "modifier" tout en bas de la page qui va nous guider vers une quatrième page. Ces pages peuvent être utilisées pour gérer des informations sur les Lauréats du Prix Nobel, telles que les listes des id, les noms, les dates de naissance , les sexes et les nationalités. La page "afficher" peut être utilisée pour afficher les informations actuelles, la page "supprimer" peut être utilisée pour supprimer des éléments de la liste, et pour permettre aux utilisateurs de modifier les informations existantes, la page "afficher" inclut également le bouton "modifier". En cliquant sur ce bouton, les utilisateurs peuvent accéder à la page de modification correspondante, où ils peuvent modifier les informations de l'élément sélectionné. Quant à la page "ajouter", elle peut être utilisée pour ajouter des informations spécifiques.


<br>
    La troisième rubrique qui nous représente le "Prix" est similaire à la deuxième en ce qui concerne les pages qu'on pourra sélectionner, car elle permet également de gérer les informations concernant le prix Nobel de la même façon précédente à laide des quatres pages "afficher", "supprimer", "modifier" et "ajouter". La page afficher se charge donc de l'affichage des informations qui ont lien avec le prix Nobel telles que les id des différents prix, les disciplines, les montants et les comités, la page "supprimer" sert à supprimer des éléments spécifiques de la liste, la page "ajouter" est utilisée pour ajouter de nouvelles informations à la liste, et le bouton "modifier" nous guide pour modifier les informations de l'élément sélectionné.

<br>

    Quant à la dernière rubrique placée tout en haut à droite, elle est dédiée à la gestion des informations relatives à "gagner" qui s’ agissent des id des lauréats, les id des prix et l'année de leurs décernation. Cette rubrique permet de même aux utilisateurs d'effectuer les trois actions différentes "afficher", "modifier", "supprimer" et "ajouter".





    En résumé, la syntaxe de manipulation du site Web a une rubrique d'accueil et trois rubriques dédiées à la gestion des informations, chacune avec plusieurs pages pour effectuer différentes actions sur ces informations.</p>
